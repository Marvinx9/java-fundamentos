package wrapper;

public class Wrapper {

  public static void main(String[] args) {
    // tipo primitivo ocupa menos memória que um wrapper
    int tipoInt = 1;
    float tipoFloat = 1;
    boolean tipoBoolean = false;

    // wrapper reconhece as classe aplicáveis para um tipo
    Integer tipoInteger = 1;
    Float tipoFloat2 = 1F;
    Boolean tipBoolean2 = false;
  }
}
